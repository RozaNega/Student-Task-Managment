using Microsoft.AspNetCore.Identity;
using Microsoft.EntityFrameworkCore;
using userapp.Data;
using userapp.Models;

var builder = WebApplication.CreateBuilder(args);

// ================= ADD SERVICES =================

builder.Services.AddControllersWithViews();

builder.Services.AddDbContext<AppDbContext>(options =>
    options.UseSqlServer(
        builder.Configuration.GetConnectionString("DefaultConnection")));

builder.Services.AddIdentity<users, IdentityRole>(options =>
{
    // Password settings
    options.Password.RequireDigit = true;
    options.Password.RequiredLength = 8;
    options.Password.RequireNonAlphanumeric = false;
    options.Password.RequireUppercase = true;
    options.Password.RequireLowercase = false;

    // Sign-in settings
    options.SignIn.RequireConfirmedAccount = false;   // changed to false (important)
    options.SignIn.RequireConfirmedEmail = false;     // changed to false
    options.SignIn.RequireConfirmedPhoneNumber = false; // changed to false
})
.AddEntityFrameworkStores<AppDbContext>()
.AddDefaultTokenProviders();

var app = builder.Build();

// ================ SEED ROLES AND DEFAULT ADMIN ================
using (var scope = app.Services.CreateScope())
{
    var services = scope.ServiceProvider;
    try
    {
        // Ensure database migrations are applied before seeding
        var db = services.GetRequiredService<AppDbContext>();
        db.Database.Migrate();

        var roleManager = services.GetRequiredService<RoleManager<IdentityRole>>();
        var userManager = services.GetRequiredService<UserManager<users>>();
        var config = services.GetRequiredService<IConfiguration>();

        // Ensure roles exist
        string[] roles = new[] { "Admin", "Storeman", "Employee", "Customer" };
        foreach (var role in roles)
        {
            if (!roleManager.RoleExistsAsync(role).GetAwaiter().GetResult())
            {
                roleManager.CreateAsync(new IdentityRole(role)).GetAwaiter().GetResult();
            }
        }

        // Seed default admin user if none exists in Admin role
        var adminEmail = config["AdminUser:Email"];
        var adminPassword = config["AdminUser:Password"];

        if (!string.IsNullOrEmpty(adminEmail) && !string.IsNullOrEmpty(adminPassword))
        {
            var adminUser = userManager.FindByEmailAsync(adminEmail).GetAwaiter().GetResult();
            if (adminUser == null)
            {
                var admin = new users
                {
                    UserName = adminEmail,
                    Email = adminEmail,
                    FullName = "Administrator"
                };

                var result = userManager.CreateAsync(admin, adminPassword).GetAwaiter().GetResult();
                if (result.Succeeded)
                {
                    userManager.AddToRoleAsync(admin, "Admin").GetAwaiter().GetResult();
                }
            }
            else
            {
                // Ensure user is in Admin role
                if (!userManager.IsInRoleAsync(adminUser, "Admin").GetAwaiter().GetResult())
                {
                    userManager.AddToRoleAsync(adminUser, "Admin").GetAwaiter().GetResult();
                }
            }
        }
    }
    catch (Exception ex)
    {
        // Log errors if needed (no logger available here)
        Console.WriteLine("Error seeding roles/admin: " + ex.Message);
    }
}

// ================= MIDDLEWARE PIPELINE =================

if (!app.Environment.IsDevelopment())
{
    app.UseExceptionHandler("/Home/Error");
    app.UseHsts();
}

app.UseHttpsRedirection();
app.UseStaticFiles();   // Important for CSS, JS, Bootstrap

app.UseRouting();

app.UseAuthentication();   // MUST come before Authorization
app.UseAuthorization();

app.MapControllerRoute(
    name: "default",
    pattern: "{controller=Home}/{action=Index}/{id?}");

app.Run();